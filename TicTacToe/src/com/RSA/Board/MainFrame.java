    private void itemUndoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemUndoActionPerformed
        int r,c;
        
        
        if(nxtmv.size() > 1)
        {
            r = nxtmv.get(nxtmv.size() - 1).getRow();
            c = nxtmv.get(nxtmv.size() - 1).getColumn();
        
            computerMove.setPlayer('B', r, c);
            setBoard(r, c, true, null);
            nxtmv.remove(nxtmv.size() - 1);
            depth = depth + 1;
        
            r = nxtmv.get(nxtmv.size() - 1).getRow();
            c = nxtmv.get(nxtmv.size() - 1).getColumn();
        
            computerMove.setPlayer('B', r, c);
            setBoard(r, c, true, null);
            nxtmv.remove(nxtmv.size() - 1);
            depth = depth + 1;
        }
        
    }//GEN-LAST:event_itemUndoActionPerformed

