    private boolean setPlayerMove(JButton btn, int x, int y, String str)
    {
        btn.setText(str);
        btn.setEnabled(false);
        computerMove.setPlayer(str.charAt(0), x, y);
        depth = depth - 1;
        
        NextMove mv = new NextMove();
        mv.setRow(x);
        mv.setColumn(y);
        nxtmv.add(mv);
        
       return computerMove.isWin(str.charAt(0));
        
    }
    
    private boolean setComputerMove()
    {
        move = computerMove.minmax(depth, 'O', -200, 200);
        computerMove.setPlayer('O', move[1], move[2]);
        setBoard(move[1], move[2], false, "0");
        depth = depth - 1;
        
        NextMove mv = new NextMove();
        mv.setRow(move[1]);
        mv.setColumn(move[2]);
        nxtmv.add(mv);
        
        return computerMove.isWin('O');
    }
    
    private void setBoard(int r, int c, boolean check, String text)
    {
        if(r == 0 && c == 0)
        {
            Btn00.setEnabled(check);
            Btn00.setText(text);
        }
        else if(r == 0 && c == 1)
        {
            Btn01.setEnabled(check);
            Btn01.setText(text);
        }
        else if(r == 0 && c == 2)
        {
            Btn02.setEnabled(check);
            Btn02.setText(text);
        }
        else if(r == 1 && c == 0)
        {
            Btn10.setEnabled(check);
            Btn10.setText(text);
        }
            
        else if(r == 1 && c == 1)
        {
            Btn11.setEnabled(check);
            Btn11.setText(text);
        }
        else if(r == 1 && c == 2)
        {
            Btn12.setEnabled(check);
            Btn12.setText(text);
        }
        else if(r == 2 && c == 0)
        {
            Btn20.setEnabled(check);
            Btn20.setText(text);
        }
        else if(r == 2 && c == 1)
        {
            Btn21.setEnabled(check);
            Btn21.setText(text);
        }
        else if(r == 2 && c == 2)
        {
            Btn22.setEnabled(check);
            Btn22.setText(text);
        }
        else
            return;
        
    }
    
    private void setWinMessage(String Message)
    {
        JOptionPane.showMessageDialog(null, Message, "Result", JOptionPane.INFORMATION_MESSAGE);
        game_Initialize();
    }
            
    
    private void move(JButton btn, int r, int c)
    {
        if(GameType == false)
         {
             if(setPlayerMove(btn, r, c, "X") == true)
             {    
                 setWinMessage("You win the game.");
                 score.updateResult(0);
                 return;
             }
             else if(depth == 0)
             {
                 setWinMessage("Match Drawn.");
                 score.updateResult(1);
                 return;
             }
         
             if(setComputerMove() == true)
             {
                 setWinMessage("Computer win the game.");
                 score.updateResult(2);
                 return;
             }
         }
        
         else if(depth % 2 == 1)
         {
             if(setPlayerMove(btn, r, c, "X") == true)
             {
                 setWinMessage("Player 1 win the game.");
                 score.updateResult(0);
                 return;
             }
             else if(depth == 0)
             {
                 setWinMessage("Match Drawn.");
                 score.updateResult(1);
                 return;
             }
         }
         else
         {
             if(setPlayerMove(btn, r, c, "O") == true)
             {
                 setWinMessage("Player 2 win the game.");
                 score.updateResult(2);
                 return;
                         
             }
         }
        
    }
    
